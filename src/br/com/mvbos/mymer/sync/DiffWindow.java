    private final List<Field> leftField = new ArrayList<>();
    private final List<Field> rightField = new ArrayList<>();

    private final List<IndexElement> leftIndex = new ArrayList<>();
    private final List<IndexElement> rightIndex = new ArrayList<>();

    private TableElement lSel;
    private TableElement rSel;

    private void updateTextDiff() {
        Differ.clear();

        Differ.compareField(lSel, leftField, rightField);
        Differ.compareIndex(lSel, leftIndex, rightIndex);
        textDiffLeft.setText(Differ.getString());

        Differ.clear();
        Differ.compareField(rSel, rightField, leftField);
        Differ.compareIndex(rSel, rightIndex, leftIndex);
        textDiffRight.setText(Differ.getString());
    }

    private void clear(boolean left) {
        if (left) {
            this.lSel = null;
            leftField.clear();
            leftIndex.clear();
            lblLeftBase.setText(null);
            lblLefTable.setText(null);
        } else {
            this.rSel = null;
            rightField.clear();
            rightIndex.clear();
            lblRightBase.setText(null);
            lblRightTable.setText(null);
        }
    }

    public void setLeft(String base, Table sel) {
    }

    public void setLeft(TableElement sel) {

        clear(true);

        this.lSel = sel;

        if (sel != null) {
            lblLeftBase.setText(sel.getDataBase().getName());
            lblLefTable.setText(sel.getName());

            leftField.addAll(sel.getFields());

            IndexEntity ie = EntityManager.e().getEntity(IndexEntity.class);
            leftIndex.addAll(ie.findIndexByTable(sel));

            updateTextDiff();
        }

    }

    public void setRight(String base, Table sel) {

        clear(false);

        if (sel != null) {
            this.rSel = new TableElement(new DataBaseElement(base, Color.BLUE), sel);

            lblRightBase.setText(base);
            lblRightTable.setText(sel.getName());

            rightField.addAll(sel.getFields());

            if (sel.getIndices() != null) {
                for (Index i : sel.getIndices()) {
                    rightIndex.add(new IndexElement(i, rSel));
                }
            }

            updateTextDiff();
        }
    }

    public void setRight(TableElement sel) {
        clear(false);
        this.rSel = sel;

        if (sel != null) {
            lblRightBase.setText(sel.getDataBase().getName());
            lblRightTable.setText(sel.getName());

            rightField.addAll(sel.getFields());

            IndexEntity ie = EntityManager.e().getEntity(IndexEntity.class);
            rightIndex.addAll(ie.findIndexByTable(sel));

            updateTextDiff();
        }
    }

    private void clear() {
        leftField.clear();
        rightField.clear();
    }

    private boolean hasChange(Field fa, Field fb) {
        if (!fa.equals(fb)) {
            return false;
        }

        java.lang.reflect.Field[] fields = fa.getClass().getDeclaredFields();

        for (java.lang.reflect.Field fl : fields) {
            try {
                java.lang.reflect.Field fr = fb.getClass().getDeclaredField(fl.getName());
                fl.setAccessible(true);
                fr.setAccessible(true);

                if (fl.get(fa) == null || fr.get(fb) == null) {
                    continue;
                }

                if (!fl.get(fa).equals(fr.get(fb))) {
                    return true;
                }

            } catch (NoSuchFieldException | SecurityException | IllegalArgumentException | IllegalAccessException ex) {
                Logger.getLogger(DiffWindow.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

        return false;
    }

    public void loadFormHistory(final TableElement sel) {
        new Thread() {

            @Override
            public void run() {
                resetView();

                setLeft(sel);
                bar.setValue(50);

                Table tb = FileUtil.open(sel.getDataBase().getName(), sel.getName());
                //Table tb = FileUtil.open(String.format("%s.%s", sel.getDataBase().getName(), sel.getName()));
                bar.setValue(75);

                setRight(sel.getDataBase().getName(), tb);
                bar.setValue(100);

                lblOrgLeft.setText("Local");
                lblOrgRight.setText("Local History");
            }

        }.start();
    }

    private void resetView() {
        bar.setValue(0);
        lblRightBase.setText("Loading...");
        lblRightTable.setText("Loading...");
        lblRightBase.setText("Loading...");
        lblRightTable.setText("Loading...");

        lblOrgLeft.setText("");
        lblOrgRight.setText("");
    }
