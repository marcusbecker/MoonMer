    private TableElement copy(TableElement t, ViewTable v) {
        int ppx = v.getPx() == 0 ? px : v.getPx();

        TableElement copy = new TableElement(ppx, v.getPy(), t.getWidth(), t.getHeight(), t.getDataBase(), t.getName());
        copy.setFields(t.getFields());
        copy.update();

        px += copy.getWidth() + 10;

        return copy;
    }
